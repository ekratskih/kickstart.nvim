  pattern = { 'javascript', 'typescript', 'javascriptreact', 'typescriptreact', 'css', 'scss', 'html', 'json', 'yaml' },
  callback = function()
    vim.bo.expandtab = true -- use spaces instead of tabs
    vim.bo.tabstop = 2 -- show tabs as 2 spaces
